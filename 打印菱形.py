# 思路：
# 行号	个数		前空格		总空格数
#  1	   1		3			6
#  2	   3		2			4
#  3	   5		1			2
#  4	   7		0			0
#  5	   5		1			2
#  6	   3		2			4
#  7	   1		3			6
# 这是中心对称的
# 我们关注的是一行如果打满星号总共的数量与前空格一列，每行菱形前面的空格从3-0,再从0-3
n = int(input("pls input n: "))
w = n // 2
if n % 2 == 0:
    n += 1
for i in range(-w, w+1):
    if i < 0:
        aaa = -i
    else:
        aaa = i
    print(aaa * ' ' + (n - aaa*2) * '*')